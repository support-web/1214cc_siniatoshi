export function calculateInvestmentType(totalScore) {
  if (totalScore >= 8 && totalScore <= 13) {
    return 'stable';
  } else if (totalScore >= 14 && totalScore <= 18) {
    return 'prudent';
  } else if (totalScore >= 19 && totalScore <= 24) {
    return 'balanced';
  } else if (totalScore >= 25 && totalScore <= 28) {
    return 'growth';
  } else {
    return 'aggressive';
  }
}
